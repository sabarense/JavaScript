function soma(valorA,valorB){
    return valorA + valorB;
}

console.log("Soma = " + soma(3,4));

function subtracao(valorA,valorB){
    return valorA - valorB;
}

console.log("Subtracao = " + subtracao(3,4));

function multiplicacao(valorA,valorB){
    return valorA * valorB;
}

console.log("Multiplicacao = " + multiplicacao(3,4));

function divisao(valorA,valorB){
    return valorA / valorB;
}

console.log("Divisao = " + divisao(3,4));